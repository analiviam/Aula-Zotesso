      <h3>Robert Down Junior irá interpretar Dolittle</h3>
      <img src="https://br.web.img3.acsta.net/pictures/19/10/14/16/18/3699651.jpg" alt="">
      <p>O ator Robert Downey Jr. passou os últimos onze anos no papel de Tony Stark, o Homem de Ferro, super-herói tecnológico que defendeu a humanidade de todos os tipos de vilões. Em "Dolittle", seu primeiro filme após 5 seguidos do universo Marvel, ele embarca em um novo tipo de aventura. Dá vida ao médico inglês que fala com animais, um clássico da literatura várias vezes adaptado para o cinema.</p>
      <p>Ele conta ao Fantástico que o principal desafio foi contracenar com animais falantes que não existiam na filmagem - foram inseridos através de efeitos especiais. No filme, a fiel conselheira do Doutor Dolittle é Polinesya. Apesar do nome, ela se parece com a nossa Arara Canindé, encontrada na Amazônia brasileira.</p>
      <p>O filme não diz qual a origem dela. Mesmo assim o ator - que também é o produtor executivo do longa – se animou com a ideia de incluir animais da fauna brasileira numa possível continuação. Downey Jr. diz que está muito orgulhoso por poder passar uma mensagem de respeito aos animais</p>
